public class Estudiante {
    private String nombre; // Nombre del estudiante
    private int nota; // Nota del estudiante

    // Constructor
    public Estudiante(String nombre, int nota) {
        this.nombre = nombre;
        this.nota = nota;
    }

    // Getters
    public String getNombre() {
        return nombre;
    }

    public int getNota() {
        return nota;
    }
    
}
